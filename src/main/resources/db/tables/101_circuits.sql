CREATE TABLE CIRCUITS
(
    CIRCUIT_ID  NUMBER(19, 0) NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    CIRCUIT_REF VARCHAR(255),
    NAME        VARCHAR(255),
    LOCATION    VARCHAR(255),
    COUNTRY     VARCHAR(255),
    LAT         DOUBLE,
    LNG         DOUBLE,
    ALT         VARCHAR(255),
    URL         VARCHAR(255),
    PRIMARY KEY (CIRCUIT_ID)
)